// 根据国家代码或国家名获取洲
export function getContinentFromCountry(countryCodeOrName: string): string {
  // 先尝试直接匹配
  if (COUNTRY_TO_CONTINENT[countryCodeOrName]) {
    return COUNTRY_TO_CONTINENT[countryCodeOrName];
  }
  
  // 尝试转换为大写后匹配
  const upperCode = countryCodeOrName.toUpperCase();
  if (COUNTRY_TO_CONTINENT[upperCode]) {
    return COUNTRY_TO_CONTINENT[upperCode];
  }
  
  // 未找到，返回"其他"
  return '其他';
}

// 使用 ip-api.com 获取地理位置信息
export interface GeoLocation {
  status: string;
  country?: string;
  countryCode?: string;
  region?: string;
  regionName?: string;
  city?: string;
  lat?: number;
  lon?: number;
  timezone?: string;
  isp?: string;
  org?: string;
  as?: string;
  query?: string;
  message?: string;
}

// 从浏览器获取地理位置（通过IP）
export async function fetchGeoLocation(): Promise<GeoLocation | null> {
  try {
    const response = await fetch('http://ip-api.com/json/?fields=status,country,countryCode,region,regionName,city');
    if (!response.ok) {
      console.error('Failed to fetch geo location');
      return null;
    }
    const data = await response.json();
    return data as GeoLocation;
  } catch (error) {
    console.error('Error fetching geo location:', error);
    return null;
  }
}

